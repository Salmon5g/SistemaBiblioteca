    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String isbn = txtIsbn.getText().trim();
        String titulo = txtTitulo.getText().trim();
        String ejemplaresStr = txtEjemplares.getText().trim();
        if (isbn.isEmpty() || titulo.isEmpty() || ejemplaresStr.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "ISBN, Título y Ejemplares son obligatorios.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (cbAutor.getSelectedItem() == null || cbCategoria.getSelectedItem() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe registrar y seleccionar un Autor y Categoría.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            int ejemplares = Integer.parseInt(ejemplaresStr);
            com.mycompany.sistemaBiblioteca.Logica.Libro l = new com.mycompany.sistemaBiblioteca.Logica.Libro();
            if (!txtId.getText().isEmpty()) {
                l.setIdLibro(Integer.parseInt(txtId.getText()));
            }
            l.setIsbn(isbn);
            l.setTitulo(titulo);
            if (!txtAnio.getText().trim().isEmpty()) {
                l.setAnioPublicacion(Integer.parseInt(txtAnio.getText().trim()));
            }
            l.setEditorial(txtEditorial.getText().trim());
            l.setCantidadEjemplares(ejemplares);
            l.setAutor((com.mycompany.sistemaBiblioteca.Logica.Autor) cbAutor.getSelectedItem());
            l.setCategoria((com.mycompany.sistemaBiblioteca.Logica.Categoria) cbCategoria.getSelectedItem());
            com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().guardarLibro(l);
            javax.swing.JOptionPane.showMessageDialog(this, "Libro guardado correctamente.", "Información", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            llenarTabla();
            limpiarCampos();
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Año y Ejemplares deben ser numéricos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (txtId.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un libro para eliminar.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        int respuesta = javax.swing.JOptionPane.showConfirmDialog(this, "¿Seguro de eliminar el libro?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
        if (respuesta == javax.swing.JOptionPane.YES_OPTION) {
            try {
                Integer id = Integer.parseInt(txtId.getText());
                com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().eliminarLibro(id);
                javax.swing.JOptionPane.showMessageDialog(this, "Libro eliminado correctamente.", "Información", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                llenarTabla();
                limpiarCampos();
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

