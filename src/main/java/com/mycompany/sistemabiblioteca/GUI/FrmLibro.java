    private void llenarComboBoxes() {
        cbAutor.removeAllItems();
        cbCategoria.removeAllItems();

        java.util.ArrayList<com.mycompany.sistemaBiblioteca.Logica.Autor> autores
                = com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().listarAutores();
        for (com.mycompany.sistemaBiblioteca.Logica.Autor a : autores) {
            cbAutor.addItem(a);
        }

        java.util.ArrayList<com.mycompany.sistemaBiblioteca.Logica.Categoria> categorias
                = com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().listarCategorias();
        for (com.mycompany.sistemaBiblioteca.Logica.Categoria c : categorias) {
            cbCategoria.addItem(c);
        }
    }

    private void llenarTabla() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblLibros.getModel();
        model.setRowCount(0);
        java.util.ArrayList<com.mycompany.sistemaBiblioteca.Logica.Libro> lista
                = com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().listarLibros();
        for (com.mycompany.sistemaBiblioteca.Logica.Libro l : lista) {
            Object[] fila = {
                l.getIdLibro(),
                l.getIsbn(),
                l.getTitulo(),
                l.getAnioPublicacion(),
                l.getCantidadEjemplares(),
                l.getCantidadDisponible(),
                l.getAutor() != null ? l.getAutor().getNombreCompleto() : "S/A",
                l.getCategoria() != null ? l.getCategoria().getNombre() : "S/C"
            };
            model.addRow(fila);
        }
    }

    private void cargarLibroSeleccionado() {
        int fila = tblLibros.getSelectedRow();
        if (fila != -1) {
            Integer id = Integer.parseInt(tblLibros.getValueAt(fila, 0).toString());
            com.mycompany.sistemaBiblioteca.Logica.Libro l
                    = com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().buscarLibroPorId(id);
            if (l != null) {
                txtId.setText(l.getIdLibro().toString());
                txtIsbn.setText(l.getIsbn());
                txtTitulo.setText(l.getTitulo());
                txtAnio.setText(l.getAnioPublicacion() != null ? l.getAnioPublicacion().toString() : "");
                txtEditorial.setText(l.getEditorial() != null ? l.getEditorial() : "");
                txtEjemplares.setText(l.getCantidadEjemplares() != null ? l.getCantidadEjemplares().toString() : "1");

                // Buscar y seleccionar el Autor
                if (l.getAutor() != null) {
                    for (int i = 0; i < cbAutor.getItemCount(); i++) {
                        if (cbAutor.getItemAt(i).getIdAutor().equals(l.getAutor().getIdAutor())) {
                            cbAutor.setSelectedIndex(i);
                            break;
                        }
                    }
                }

                // Buscar y seleccionar la Categoría
                if (l.getCategoria() != null) {
                    for (int i = 0; i < cbCategoria.getItemCount(); i++) {
                        if (cbCategoria.getItemAt(i).getIdCategoria().equals(l.getCategoria().getIdCategoria())) {
                            cbCategoria.setSelectedIndex(i);
                            break;
                        }
                    }
                }
            }
        }
    }

    private void limpiarCampos() {
        txtId.setText("");
        txtIsbn.setText("");
        txtTitulo.setText("");
        txtAnio.setText("");
        txtEditorial.setText("");
        txtEjemplares.setText("");
        if (cbAutor.getItemCount() > 0) {
            cbAutor.setSelectedIndex(0);
        }
        if (cbCategoria.getItemCount() > 0) {
            cbCategoria.setSelectedIndex(0);
        }
        tblLibros.clearSelection();
    }


