    private void llenarTabla() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblSocios.getModel();
        model.setRowCount(0);
        java.util.ArrayList<com.mycompany.sistemaBiblioteca.Logica.Socio> lista
                = com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().listarSocios();
        for (com.mycompany.sistemaBiblioteca.Logica.Socio s : lista) {
            Object[] fila = {
                s.getIdSocio(),
                s.getRut(),
                s.getNombreCompleto(),
                s.getTelefono(),
                s.getEmail(),
                s.getEstado()
            };
            model.addRow(fila);
        }
    }

    private void cargarSocioSeleccionado() {
        int fila = tblSocios.getSelectedRow();
        if (fila != -1) {
            txtId.setText(tblSocios.getValueAt(fila, 0).toString());
            txtRut.setText(tblSocios.getValueAt(fila, 1).toString());
            txtNombre.setText(tblSocios.getValueAt(fila, 2).toString());
            txtTelefono.setText(tblSocios.getValueAt(fila, 3) != null ? tblSocios.getValueAt(fila, 3).toString() : "");
            txtEmail.setText(tblSocios.getValueAt(fila, 4) != null ? tblSocios.getValueAt(fila, 4).toString() : "");

            Integer id = Integer.parseInt(txtId.getText());
            com.mycompany.sistemaBiblioteca.Logica.Socio s
                    = com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().buscarSocioPorId(id);
            if (s != null) {
                txtDireccion.setText(s.getDireccion() != null ? s.getDireccion() : "");
                cbEstado.setSelectedItem(s.getEstado());
            }
        }
    }

    private void limpiarCampos() {
        txtId.setText("");
        txtRut.setText("");
        txtNombre.setText("");
        txtTelefono.setText("");
        txtEmail.setText("");
        txtDireccion.setText("");
        cbEstado.setSelectedIndex(0);
        tblSocios.clearSelection();
    }


