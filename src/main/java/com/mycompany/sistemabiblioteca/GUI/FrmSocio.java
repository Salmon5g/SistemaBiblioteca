    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String rut = txtRut.getText().trim();
        String nombre = txtNombre.getText().trim();
        if (rut.isEmpty() || nombre.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "El RUT y el Nombre son obligatorios.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        com.mycompany.sistemaBiblioteca.Logica.Socio s = new com.mycompany.sistemaBiblioteca.Logica.Socio();
        if (!txtId.getText().isEmpty()) {
            s.setIdSocio(Integer.parseInt(txtId.getText()));
        }
        s.setRut(rut);
        s.setNombreCompleto(nombre);
        s.setTelefono(txtTelefono.getText().trim());
        s.setEmail(txtEmail.getText().trim());
        s.setDireccion(txtDireccion.getText().trim());
        s.setEstado(cbEstado.getSelectedItem().toString());
        try {
            com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().guardarSocio(s);
            javax.swing.JOptionPane.showMessageDialog(this, "Socio guardado correctamente.", "Información", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            llenarTabla();
            limpiarCampos();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (txtId.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un socio de la lista para eliminar.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        int respuesta = javax.swing.JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar al socio?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
        if (respuesta == javax.swing.JOptionPane.YES_OPTION) {
            try {
                Integer id = Integer.parseInt(txtId.getText());
                com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().eliminarSocio(id);
                javax.swing.JOptionPane.showMessageDialog(this, "Socio eliminado correctamente.", "Información", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                llenarTabla();
                limpiarCampos();
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

