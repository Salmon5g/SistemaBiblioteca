    private void actualizarCombosYTablas() {
        // 1. Cargar Socios activos
        cbSocio.removeAllItems();
        java.util.ArrayList<com.mycompany.sistemaBiblioteca.Logica.Socio> socios
                = com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().listarSocios();
        for (com.mycompany.sistemaBiblioteca.Logica.Socio s : socios) {
            if ("Activo".equalsIgnoreCase(s.getEstado())) {
                cbSocio.addItem(s);
            }
        }

        // 2. Cargar Libros disponibles
        cbLibro.removeAllItems();
        java.util.ArrayList<com.mycompany.sistemaBiblioteca.Logica.Libro> libros
                = com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().listarLibrosDisponibles();
        for (com.mycompany.sistemaBiblioteca.Logica.Libro l : libros) {
            cbLibro.addItem(l);
        }

        // 3. Cargar Tabla de Préstamos Activos
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblPrestamosActivos.getModel();
        model.setRowCount(0);
        java.util.ArrayList<com.mycompany.sistemaBiblioteca.Logica.Prestamo> activos
                = com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().listarPrestamosActivos();
        for (com.mycompany.sistemaBiblioteca.Logica.Prestamo p : activos) {
            Object[] fila = {
                p.getIdPrestamo(),
                p.getSocio().getNombreCompleto(),
                p.getLibro().getTitulo(),
                dateFormat.format(p.getFechaDevolucionPrevista())
            };
            model.addRow(fila);
        }
    }

    private void cargarPrestamoSeleccionado() {
        int fila = tblPrestamosActivos.getSelectedRow();
        if (fila != -1) {
            txtIdPrestamoDev.setText(tblPrestamosActivos.getValueAt(fila, 0).toString());
            txtSocioDev.setText(tblPrestamosActivos.getValueAt(fila, 1).toString());
            txtLibroDev.setText(tblPrestamosActivos.getValueAt(fila, 2).toString());
            txtFechaPrevistaDev.setText(tblPrestamosActivos.getValueAt(fila, 3).toString());
            txtFechaRealDev.setText(dateFormat.format(new java.util.Date()));
            txtMultaDev.setText("$0.0");
        }
    }

    private void limpiarCamposDevolucion() {
        txtIdPrestamoDev.setText("");
        txtSocioDev.setText("");
        txtLibroDev.setText("");
        txtFechaPrevistaDev.setText("");
        txtFechaRealDev.setText(dateFormat.format(new java.util.Date()));
        txtMultaDev.setText("$0.0");
        tblPrestamosActivos.clearSelection();
    }

