    private void btnRegistrarPrestamoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarPrestamoActionPerformed
        com.mycompany.sistemaBiblioteca.Logica.Socio s = (com.mycompany.sistemaBiblioteca.Logica.Socio) cbSocio.getSelectedItem();
        com.mycompany.sistemaBiblioteca.Logica.Libro l = (com.mycompany.sistemaBiblioteca.Logica.Libro) cbLibro.getSelectedItem();
        if (s == null || l == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar Socio y Libro.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            java.util.Date fPrestamo = dateFormat.parse(txtFechaPrestamo.getText().trim());
            java.util.Date fPrevista = dateFormat.parse(txtFechaPrevista.getText().trim());
            if (fPrevista.before(fPrestamo)) {
                javax.swing.JOptionPane.showMessageDialog(this, "La fecha de devolución no puede ser anterior al préstamo.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }
            com.mycompany.sistemaBiblioteca.Logica.Prestamo p = new com.mycompany.sistemaBiblioteca.Logica.Prestamo();
            p.setSocio(s);
            p.setLibro(l);
            p.setFechaPrestamo(fPrestamo);
            p.setFechaDevolucionPrevista(fPrevista);
            com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().registrarPrestamo(p);
            javax.swing.JOptionPane.showMessageDialog(this, "¡Préstamo registrado con éxito!", "Información", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            // El Observer actualizará el stock e inventarios automáticamente
        } catch (java.text.ParseException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Fecha inválida. Use AAAA-MM-DD.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch (IllegalStateException e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage(), "Negocio", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarPrestamoActionPerformed

    private void btnCalcularMultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularMultaActionPerformed
        if (txtIdPrestamoDev.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un préstamo de la lista.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            java.util.Date prevista = dateFormat.parse(txtFechaPrevistaDev.getText());
            java.util.Date real = dateFormat.parse(txtFechaRealDev.getText().trim());
            double multa = com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().calcularMulta(prevista, real);
            txtMultaDev.setText("$" + multa);
        } catch (java.text.ParseException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Fecha real inválida.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCalcularMultaActionPerformed

    private void btnRegistrarDevolucionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarDevolucionActionPerformed
        if (txtIdPrestamoDev.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un préstamo de la lista.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            Integer id = Integer.parseInt(txtIdPrestamoDev.getText());
            java.util.Date real = dateFormat.parse(txtFechaRealDev.getText().trim());
            java.util.Date prevista = dateFormat.parse(txtFechaPrevistaDev.getText());

            double multa = com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().calcularMulta(prevista, real);
            com.mycompany.sistemaBiblioteca.Logica.BibliotecaFacade.getInstancia().registrarDevolucion(id, real);
            String msj = "Devolución completada.";
            if (multa > 0) {
                msj += "\nMulta generada: $" + multa;
            }

            javax.swing.JOptionPane.showMessageDialog(this, msj, "Información", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            limpiarCamposDevolucion();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarDevolucionActionPerformed

